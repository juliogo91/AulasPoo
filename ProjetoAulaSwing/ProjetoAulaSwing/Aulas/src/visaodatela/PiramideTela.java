        piramidetela.setBase(Float.parseFloat(jTextField1CampoBase.getText()));
        piramidetela.setAltura (Float.parseFloat(jTextField1CampoAltura.getText()));
        
        
        String saida = "Dados digitados no campo: \n";
         
